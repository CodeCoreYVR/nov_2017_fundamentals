// the % modulo or modulus operator gives the result of dividing one number
// by another number. If the number is perfectly divisible by the other number
// then we should get 0

10 % 3 // this will give me 1
10 % 2 // this will give us 0

// to do more complex number operations, we can use the Math library.

Math.random() // this will give us random number that is 0 or more but less
              // than 1

Math.round(2.5) // this will give us 3

Math.ceil(2.1) // this will alway round up, so it will give us 3

Math.floor(2.7) // this will alway round down, so it will give us 2

// Write a piece of code that generates a random number between 0 and 99
Math.floor(Math.random() * 100)
